  <h1>
    Motivation
  </h1>

  <p>
    Sharing a space with other people can be challenging even when it's good friends sharing an apartment or like-minded neighbors sharing a block.
    Online social applications place thousands, millions, or billions of people
    within <em>milliseconds</em> of each other.
    How could such large and diverse groups agree on how they want their shared spaces to look and feel?
    Some people want to be engaged, others want to be disconnected.
    Some people want free speech, others want safe spaces.
    Some want to go back to the good ol' days of MySpace, others want to vibe in the metaverse.
  </p>

  <p>
    The first problem is that, as of now, social applications take a lot of money and technical expertise to build.
    Therefore the applications we "agree" to use tend be heavily biased towards the preferences of people with that money and technical expertise.
    Often these preferences include making a return on one's capital investment, usually in the form of advertisement revenue and <a href="https://www.wsj.com/articles/the-facebook-files-11631713039">usually at the expense of users</a>.
    Design justice theory also <a href="https://designjustice.mitpress.mit.edu/">provides evidence</a> that such biased design processes can systematically reproduce intersecting forms oppression including white supremacy, ableism, cisheteropatriarchy, settler colonialism, and capitalism.
  </p>

  <p>
    One of the aims of Graffiti is for everyone to be able to make their own social applications, which we hope will alleviate some of this design bias. Still, a social application only works if there is a community of people using it. People still need to agree on what they want and need.
  </p>

  <p>
    One proposal, <a href="https://www.prospectmagazine.co.uk/science-and-technology/how-we-can-take-social-media-back-from-the-billionaires-elon-musk-twitter">advocated by Ethan Zuckerman</a>, is that people should use many small social applications rather than the handful of massive social applications in use today. An application for your city, for your hobby, for your friend group. It is easier for smaller and more homogeneous communities to come to consensus. Also, if there is any fundamental disagreement that causes a community to splinter, people will have other ones to turn to.
  </p>

  <p>
    However we are skeptical that people's social lives could be so neatly divided.
    Most people's friends aren't all friends with each other &mdash; formally, <a href="https://arxiv.org/abs/1111.4503">an analysis</a> of the Facebook social graph shows that average users have a relatively low <a href="https://en.wikipedia.org/wiki/Clustering_coefficient#Local_clustering_coefficient">local clustering coefficient</a>.
    This means that for a person to maintain the same level of social ties, they might need to engage with 10 or more different social applications.
    Perhaps this is reason why, despite advocating for small self-hosted communities, the Fediverse has <a href="https://dl.acm.org/doi/abs/10.1145/3355369.3355572">tended to centralize</a> with more that half its users living on just 10% of its servers.
  </p>

  <p>
    Graffiti investigates a different solution.
    What if we could all share one space but choose to see it in different ways?
    As if it were an inkblot test,
    some people might see the space as a close circle of friends,
    some might see it as a massive community,
    and some might see it as a multiplayer video game.
    One person might have different ways of looking at the space for different purposes: one for being productive, one to veg-out, and one to spur creativity.
    Graffiti is pluralistic: one world where many worlds fit.
  </p>

